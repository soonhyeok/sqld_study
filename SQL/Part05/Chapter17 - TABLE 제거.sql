CREATE TABLE AUTHOR
(
	AUTHOR_ID INT NOT NULL PRIMARY KEY,
	FIRSTNAME VARCHAR(50),
	LASTNAME VARCHAR(50)
)
;

CREATE TABLE PAGE
(
	PAGE_ID SERIAL PRIMARY KEY,
	TITLE VARCHAR(255) NOT NULL,
	CONTENT TEXT,
	AUTHOR_ID INT NOT NULL,
	FOREIGN KEY (AUTHOR_ID)
	REFERENCES AUTHOR(AUTHOR_ID)
)
;


