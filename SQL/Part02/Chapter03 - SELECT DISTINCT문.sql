	BCOLOR, FCOLOR
;

-- DISTINCT 사용 + 컬럼 1개 + ON 사용
SELECT DISTINCT ON
	(BCOLOR) BCOLOR,
	FCOLOR
FROM 
	T1
ORDER BY
	BCOLOR, FCOLOR
;

-- DISTINCT 사용 + 컬럼 두 개 + ON 사용 + DESC 정렬
SELECT DISTINCT ON
	(BCOLOR) BCOLOR,
	FCOLOR
FROM 
	T1
ORDER BY
	BCOLOR, FCOLOR DESC
;