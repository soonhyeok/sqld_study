-- 조건 한 개
SELECT 
	LAST_NAME,
	FIRST_NAME
FROM
	CUSTOMER
WHERE
	FIRST_NAME = 'Jamie'
;

-- 조건 두 개
SELECT 
	LAST_NAME,
	FIRST_NAME
FROM
	CUSTOMER
WHERE 
	FIRST_NAME = 'Jamie'
AND LAST_NAME = 'Rice'
;

