SELECT TITLE, LENGTH
FROM FILM
WHERE LENGTH >= ANY
(
	SELECT MAX(LENGTH)
	FROM FILM A,
		 FILM_CATEGORY B
	WHERE
		A.FILM_ID = B.FILM_ID
	GROUP BY
		CATEGORY_ID
	
)
;

