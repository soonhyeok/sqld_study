SELECT
	COLUMN_1_1,
	COLUMN_1_2
FROM
	TABLE_NAME_1
INTERSECT
SELECT
	COLUMN_2_1,
	COLUMN_2_2
FROM
	TABLE_NAME_2
;

-- 두 개의 SELECT문 간 컬럼의 개수는 동일해야 하고
-- 해당 순서의 열에는 서로 호환되는 데이터 유형이어야 한다.

-- 실습
SELECT
	DISTINCT INVENTORY.FILM_ID,
	TITLE
FROM
	INVENTORY
INNER JOIN FILM
ON FILM.FILM_ID = INVENTORY.FILM_ID
ORDER BY 
	TITLE;
	
