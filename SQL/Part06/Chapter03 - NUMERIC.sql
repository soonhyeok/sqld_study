CREATE TABLE PRODUCTS
(
	ID SERIAL PRIMARY KEY,
	NAME VARCHAR NOT NULL,
	PRICE NUMERIC (5, 2)
)
;

-- PRICE 컬럼을 NUMERIC으로 지정하였으며 해당 컬럼은 999.99까지의 실수형 데이터를 담는다.

